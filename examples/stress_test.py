"""
gpushare stress test & connection validator.

Run on any client machine to verify the remote GPU connection works
and push real GPU load so you can see utilization spike on the server.

Usage:
    python stress_test.py                     # localhost
    python stress_test.py 192.168.1.100       # remote server
    python stress_test.py 192.168.1.100 --duration 30  # 30 second burn
"""

